<template>
    <b-navbar type="is-success">
        <template slot="brand">
            <b-navbar-item tag="router-link" :to="{ name: 'home' }">
                <b-icon icon="scissors-cutting" />
                <div>Yards and Co.</div>
            </b-navbar-item>
        </template>
        <template slot="start">
            <b-navbar-item tag="router-link" :to="{ name: 'fabric_list' }">
                Liste des tissus
            </b-navbar-item>
            <b-navbar-item tag="router-link" :to="{ name: 'fabric_add' }">
                Ajouter un tissu
            </b-navbar-item>
            <b-navbar-item tag="router-link" :to="{ name: 'color_list' }">
                Liste des couleurs
            </b-navbar-item>
            <b-navbar-item tag="router-link" :to="{ name: 'color_add' }">
                Ajouter une couleur
            </b-navbar-item>
        </template>

        <template slot="end" v-if="isLoaded">
            <b-navbar-dropdown v-if="user.uuid" :label="user.username" right>
                <b-navbar-item tag="router-link" :to="{ name: 'profile' }">
                    Mon compte
                </b-navbar-item>
                <b-navbar-item @click="logout">
                    Se déconnecter
                </b-navbar-item>
            </b-navbar-dropdown>
            <b-navbar-item tag="div" v-else>
                <div class="buttons">
                    <b-button
                        tag="router-link"
                        :to="{ name: 'register' }"
                        type="is-primary"
                    >
                        <strong>S'enregistrer</strong>
                    </b-button>
                    <b-button
                        tag="router-link"
                        :to="{ name: 'login' }"
                        type="is-light"
                    >
                        Se connecter
                    </b-button>
                </div>
            </b-navbar-item>
        </template>
    </b-navbar>
</template>

<script lang="ts">
import User from '../models/user';
import {API} from '../http-common';
import {defineComponent} from '@vue/composition-api';

export default defineComponent({
    name: 'Navbar',
    props: {
        user: {
            type: Object as () => User,
            required: true
        },
        isLoaded: {
            type: Boolean,
            default: false
        }
    },
    methods: {
        logout(): void {
            API.post(window.location.origin + '/api/logout').finally(() => {
                this.$root.user = new User();
                this.$buefy.toast.open({
                    duration: 3000,
                    message: `Tu as bien été déconnecté·e !`,
                    position: 'is-top',
                    type: 'is-info'
                });
            });
        }
    }
});
</script>

